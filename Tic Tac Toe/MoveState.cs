namespace Tic_Tac_Toe
{
    public enum MoveState
    {
        // This enumerator defines all possible states that can be the result of a move made by a player.

        // The win state means that the move resulted in a win.
        Win,

        // The draw state means that the move resulted to a draw.
        Draw,

        // The continue state means that the match will be continued.
        Continue
    }
}
